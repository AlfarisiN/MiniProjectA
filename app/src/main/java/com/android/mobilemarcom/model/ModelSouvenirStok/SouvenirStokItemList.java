package com.android.mobilemarcom.model.ModelSouvenirStok;

public class SouvenirStokItemList {
    public String id;
    public String t_souvenir_id;
    public String m_souvenir_id;
    public String qty;
    public String note;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getT_souvenir_id() {
        return t_souvenir_id;
    }

    public void setT_souvenir_id(String t_souvenir_id) {
        this.t_souvenir_id = t_souvenir_id;
    }

    public String getM_souvenir_id() {
        return m_souvenir_id;
    }

    public void setM_souvenir_id(String m_souvenir_id) {
        this.m_souvenir_id = m_souvenir_id;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
